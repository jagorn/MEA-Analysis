function display_good_values(handles)
handles.checkbox1.Value = handles.H.SkipRep;

handles.edit1.String = num2str(handles.H.NCheckerboard1);
handles.edit2.String = num2str(handles.H.NCheckerboard2);
handles.edit3.String = display_array_compact(handles.H.Channels);
handles.edit4.String = num2str(handles.H.Nlatency);
handles.edit5.String = handles.H.StimFilePath;
%handles.edit6.String = num2str(handles.H.StimChannel);

handles.edit8.String = num2str(handles.H.stim_freq);
handles.edit9.String = num2str(handles.H.MEA_recording_freq);

handles.edit10.String = handles.H.triggers_file;
handles.edit11.String = handles.H.spikes_file;
handles.edit12.String = handles.H.MaxLat;

contents = cellstr(get(handles.popupmenu1,'String'));
if strcmp(contents{1},handles.H.StimFileFormat)
    handles.popupmenu1.Value = 1;
else
    handles.popupmenu1.Value = 2;
end
